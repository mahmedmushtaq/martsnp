    {
        //
         $ldate = date('Y-m-d H:i:s');

        Seller::create([
            'subscription'=>$ldate,
            'user_id'=>Auth::user()->id,
        ]);

        $user = User::find(Auth::user()->id);
        $user->account_type="seller";
        $user->save();

        session()->flash("success","Congratulations! You have became a seller");

        return redirect()->back();
    }

